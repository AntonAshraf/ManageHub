    private void AddButton_categoryActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_AddButton_categoryActionPerformed
        // also add to categoryTable in the same time
        String name = category.getText();
        String descriptiont = description.getText();
        String status = (String) cstatus.getSelectedItem();
        Boolean insert = DB.insertCategory(name, descriptiont, status);
        if (insert) {
            JOptionPane.showMessageDialog(null, "Inserted");
        } else {
            JOptionPane.showMessageDialog(null, "Not Inserted");
        }
        DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
        model.setRowCount(0);
        DB.populateCategoryTable((DefaultTableModel) categoryTable.getModel());
        
    }// GEN-LAST:event_AddButton_categoryActionPerformed

    private void UpdateButton_categoryActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
       // get selected row index
         int rowIndex = categoryTable.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
            if (rowIndex != -1) {
                model.setValueAt(category.getText(), rowIndex, 1);
                model.setValueAt(description.getText(), rowIndex, 2);
                model.setValueAt(cstatus.getSelectedItem(), rowIndex, 3);
                int id = (int) model.getValueAt(rowIndex, 0);
                String name = (String) model.getValueAt(rowIndex, 1);
                String descriptiont = (String) model.getValueAt(rowIndex, 2);
                String status = (String) model.getValueAt(rowIndex, 3);
                Boolean update = DB.updateCategory(id, name, descriptiont, status);
                if (update) {
                    JOptionPane.showMessageDialog(null, "Updated");
                } else {
                    JOptionPane.showMessageDialog(null, "Not Updated");
                }
            }
            
    }// GEN-LAST:event_jButton3ActionPerformed

    private void DeleteButton_categoryActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton4ActionPerformed
        int rowIndex = categoryTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
        if (rowIndex != -1) {
            model.removeRow(rowIndex);
            int id = (int) model.getValueAt(rowIndex, 0);
            Boolean delete = DB.deleteCategory(id);
            if (delete) {
                JOptionPane.showMessageDialog(null, "Deleted");
            } else {
                JOptionPane.showMessageDialog(null, "Not Deleted");
            }
        }
    }// GEN-LAST:event_jButton4ActionPerformed

