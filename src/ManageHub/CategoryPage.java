    private void categoryTableMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) categoryTable.getModel();
        int rowNumber = categoryTable.getSelectedRow();

        Object value1 = model.getValueAt(rowNumber, 1);
        String stringValue1 = value1.toString();

        Object value2 = model.getValueAt(rowNumber, 2);
        String stringValue2 = value2.toString();

        Object value3 = model.getValueAt(rowNumber, 3);
        String stringValue3 = value3.toString();

        category.setText(stringValue1);
        description.setText(stringValue2);
        cstatus.setSelectedItem(stringValue3);
    }// GEN-LAST:event_jTable1MouseClicked

